package barcode;

public class UseHistoryDataBean {
	private String receipt_barcode;
	private String user_barcode;
	private String product_barcode;
	private String product_name;
	private String product_price;
	private int product_count;
	private String maket;
	private String buydate;
	
	public String getReceipt_barcode() {
		return receipt_barcode;
	}
	public void setReceipt_barcode(String receipt_barcode) {
		this.receipt_barcode = receipt_barcode;
	}
	public String getUser_barcode() {
		return user_barcode;
	}
	public void setUser_barcode(String user_barcode) {
		this.user_barcode = user_barcode;
	}
	public String getProduct_barcode() {
		return product_barcode;
	}
	public void setProduct_barcode(String product_barcode) {
		this.product_barcode = product_barcode;
	}
	public String getProduct_name() {
		return product_name;
	}
	public void setProduct_name(String product_name) {
		this.product_name = product_name;
	}
	public String getProduct_price() {
		return product_price;
	}
	public void setProduct_price(String product_price) {
		this.product_price = product_price;
	}
	public int getProduct_count() {
		return product_count;
	}
	public void setProduct_count(int product_count) {
		this.product_count = product_count;
	}
	public String getMaket() {
		return maket;
	}
	public void setMaket(String maket) {
		this.maket = maket;
	}
	public String getBuydate() {
		return buydate;
	}
	public void setBuydate(String buydate) {
		this.buydate = buydate;
	}
}

